# Floor Division
print(10//3)
# Modulus
print(10%3)
# Absolute Value
print(abs(-5))
# Round a number
print(round(3.14159, 2))  # Rounds to 2 decimal places

# plus sign and equal sign +=, -+=, *=, /=, //=, %=, **=    
# Incrementing a variable
number = 5
number += 2  # Equivalent to number = number + 2
print(number)  # Output: 7
# Decrementing a variable
number -= 3  # Equivalent to number = number - 3
print(number)  # Output: 4
# Multiplying a variable
number *= 2  # Equivalent to number = number * 2
print(number)  # Output: 8
# Dividing a variable
number /= 4  # Equivalent to number = number / 4
print(number)  # Output: 2.0

# f strings (formatted string literals)
name = "Alice"
age = 30
height = 1.75
# Using f-string to format output
print(f"My name is {name}, I am {age} years old, and my height is {height} meters.")
